import 'log_helper.dart';

/// Helper methods for logging
class LogHelperExtensions {
  /// Log the start of fetching data
  static void logFetchingData(String dataType) {
    LogHelper.logInfo('Fetching $dataType data from API');
  }

  /// Log when no article content is found
  static void logNoArticleContentFound() {
    LogHelper.logWarning('No article content found');
  }

  /// Log successful data fetching
  static void logDataFetchSuccess(String dataType) {
    LogHelper.logInfo('Successfully fetched $dataType data');
  }

  /// Log initiating data fetch
  static void logInitiatingDataFetch(dynamic status) {
    LogHelper.logInfo('Initiating data fetch. Current status: $status');
  }
}
